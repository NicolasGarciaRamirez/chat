<script>
    import Search from '../../Search/Views/Search'
    import PublicHeaderPostMovile from "../../Home/post/PublicHeaderPostMovile";
    import Auth from '../../../helpers/Auth'

    export default {
        props:['user'],
        name: "NavFooter",
        components:{
            Search,
            PublicHeaderPostMovile
        },
        data(){
            return{
                route_name: '',
                section: '',
                root: ''
            }
        },
        mounted(){
            Auth.initialize()
            this.route_name = window.location.href
            $('.nav-post-movile').addClass('d-none')
            let btns = document.getElementsByClassName('footer-movile-btn')
            btns.forEach(btn => {
                btn.addEventListener('click', (e) => {
                    this.changeSection(e.target, btns[0])
                })
            });

            if(this.route_name.match(`${window.location.origin}/Post/Public/Movile/`) ){
                $('[data-section="post"]').click()
            }
            this.root = window.location.href.split('/')
            this.root = this.root[this.root.length - 1]
            if(this.root === 'Search'){
                $('[data-section="search"]').click()
            }
            if(this.root === `Notifications`){
                $('[data-section="notifications"]').click()
            }

        },
        methods:{
            close(){
                if(window.location.href === `${window.location.origin}/`) {
                    if(!$('#content-header').hasClass('d-none')){
                        $('[data-section="stream"]').click()
                    }
                }else if(this.location){
                    console.log(this.location)
                }
            },
            changeSection(target, btnActive){
                let data = target.dataset
                let nav_post_movile = $('#nav-post-movile')
                let nav_search_movile = $('#nav-search-movile')
                $('.footer-movile-btn').removeClass('active')
                target.classList.add('active')

                btnActive.style.setProperty('--left', `${data.id * 100}%`)

                this.section = data.section

                if(this.section === 'stream'){
                    if(window.location.href.match(`${window.location.origin}/Post/Public/Movile/`) || window.location.href.match(`${window.location.origin}/Search`) ) {
                        this.windowReplace()
                    }
                    $('.nav-footer-movile').removeClass('active')
                    $('.nav-post-movile').addClass('d-none')
                    nav_post_movile.slideUp(100, 'linear')
                    nav_search_movile.slideUp(100, 'linear')
                    nav_post_movile.removeClass('show')
                    this.setVars(false, 'stream')
                    $('#content-header').addClass('d-none')

                }else if(this.section === 'post'){
                    if(window.location.href.match(`${window.location.origin}/Post/Public/Movile/`)) {
                        $('.nav-post-movile').removeClass('d-none')
                        $('#content-header').addClass('d-none')
                    }else{
                        $('.nav-footer-movile').addClass('active')
                        $('.nav-post-movile').removeClass('d-none')
                        $('#content-header').removeClass('d-none')
                        nav_search_movile.slideUp(100, 'linear')
                        nav_post_movile.slideDown(100, 'linear')
                        setTimeout(function(){
                            nav_post_movile.addClass('show')
                        }, 200)
                    }

                }else if(this.section === 'search'){
                    this.root === 'Search' ? '' : window.location.replace('/Search')
                }else if(this.section === 'notifications'){
                    this.root === `Notifications` ? '' : window.location.replace(`/${Auth.state.username}/Notifications`)
                }
            },
            setVars(value, type){
                let refs = this.$root.$refs

                if(type === 'search'){
                    // refs.header.view_search__movil = value
                    // refs.header.view_post__movil = value

                }else if(type === 'post'){
                    console.log(value)
                    // this.$refs.headerPostMovile.view_post__movil = value
                    // this.$refs.headerPostMovile.view_search__movil = false

                }else if(type === 'stream'){
                    refs.header.view_search__movil = false
                    refs.header.view_post__movil = false
                }
            },
            windowReplace(){
                let location = window.location.href
                if(location !== 'http://localhost:8000/' && location !== 'https://www.noisesharks.com/') window.location.replace('/')
            }

        }
    }
</script>

